  `jrACT` INT(11) UNSIGNED NOT NULL ,
  `jrSTR` INT(11) UNSIGNED NOT NULL ,
  `jrLAB` INT(11) UNSIGNED NOT NULL ,
  `jrIQ` INT(11) UNSIGNED NOT NULL ,
