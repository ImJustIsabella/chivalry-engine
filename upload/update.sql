  `jRANK` INT(11) UNSIGNED NULL DEFAULT NULL AUTO_INCREMENT ,
  `jNAME` TEXT NOT NULL ,
  `jSTART` INT(11) UNSIGNED NOT NULL ,
  `jDESC` TEXT NOT NULL ,
  `jBOSS` INT NOT NULL ,
  UNIQUE (`jRANK`)
) ENGINE = MyISAM;

